#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s != '\0' && *s != c)
	{
		++s;
	}
	if (c == '\0' || *s != '\0')
	{
		return ((char *)s);
	}
	return ((void *)0);
}
